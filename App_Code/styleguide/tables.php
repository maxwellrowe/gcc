<p>Besides the "table" class, other classes that can be added:</p>
<ul>
	<li>table-bordered</li>
	<li>table-striped</li>
	<li>table-red</li>
	<li>table-purple</li>
	<li>table-dark-gray</li>
	<li>table-striped-columns</li>
</ul>

